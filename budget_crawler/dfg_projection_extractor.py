def main():
    parser = argparse.ArgumentParser()
    parser.add_argument(
        "--manifest",
        default="data/icds_sansad_committees/manifest.jsonl",
    )
    parser.add_argument(
        "--pdf-dir",
        default="data/icds_sansad_committees/pdfs/rs",
    )
    parser.add_argument(
        "--output",
        default="data/dfg_projection_timeseries.csv",
    )
    args = parser.parse_args()

    manifest_path = Path(args.manifest)
    pdf_dir = Path(args.pdf_dir)
    output_path = Path(args.output)
    output_path.parent.mkdir(parents=True, exist_ok=True)

    # Load and filter manifest: DFG report_type + women/child in title.
    # Also accept report_type=="other" when the title explicitly names DFG/Demands for Grants
    # (e.g. Report 350 on DFG 2023-24 was mis-classified as "other").
    reports = []
    with manifest_path.open() as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            rec = json.loads(line)
            rtype = rec.get("report_type", "")
            title_lower = rec.get("title", "").lower()
            if rtype == "demands_for_grants":
                pass  # always include
            elif rtype == "other" and (
                "demands for grants" in title_lower or " dfg " in title_lower
            ):
                pass  # mis-classified DFG report (e.g. Report 350)
            else:
                continue
            if not ("women" in title_lower or "child" in title_lower):
                continue
            reports.append(rec)

    print(f"Found {len(reports)} MWCD DFG reports in manifest")

    rows = []
    hits = []
    misses = []

    for rec in sorted(reports, key=lambda r: r.get("report_no", 0)):
        report_no = rec.get("report_no")
        title = rec.get("title", "")
        year = _year_from_title(title)
        pdf_rel = rec.get("pdf_path") or ""
        if not pdf_rel:
            misses.append((report_no, year, "no pdf_path in manifest"))
            rows.append(
                dict(
                    year=year,
                    report_no=report_no,
                    mwcd_projected_cr="",
                    finance_allocated_cr="",
                    cut_pct="",
                    source_pdf="",
                    confidence="none",
                    note="no pdf_path in manifest",
                )
            )
            continue
        # pdf_path in manifest is relative to data dir; resolve from pdf-dir
        pdf_filename = Path(pdf_rel).name
        pdf_path = pdf_dir / pdf_filename

        if not pdf_path.exists():
            print(f"  MISSING PDF: {pdf_filename}")
            misses.append((report_no, year, "pdf not found"))
            rows.append(
                dict(
                    year=year,
                    report_no=report_no,
                    mwcd_projected_cr="",
                    finance_allocated_cr="",
                    cut_pct="",
                    source_pdf=pdf_filename,
                    confidence="none",
                    note="pdf not found",
                )
            )
            continue

        if report_no in _MANUAL_OVERRIDES:
            ov = _MANUAL_OVERRIDES[report_no]
            projected, allocated, confidence, note = (
                ov["mwcd_projected_cr"],
                ov["finance_allocated_cr"],
                ov["confidence"],
                ov["note"],
            )
        else:
            projected, allocated, confidence, note = extract_from_pdf(pdf_path)

        cut_pct = ""
        if projected and allocated and projected > 0:
            cut_pct = round((allocated - projected) / projected * 100, 2)

        rows.append(
            dict(
                year=year,
                report_no=report_no,
                mwcd_projected_cr=projected if projected else "",
                finance_allocated_cr=allocated if allocated else "",
                cut_pct=cut_pct,
                source_pdf=pdf_filename,
                confidence=confidence,
                note=note or "",
            )
        )

        if confidence in ("high", "medium", "low"):
            hits.append((report_no, year, confidence, projected, allocated, cut_pct))
        else:
            misses.append((report_no, year, note))

    # Write CSV
    fieldnames = [
        "year",
        "report_no",
        "mwcd_projected_cr",
        "finance_allocated_cr",
        "cut_pct",
        "source_pdf",
        "confidence",
        "note",
    ]
    with output_path.open("w", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)

    print(f"\nOutput written to {output_path}")
    print(f"\n{'='*60}")
    print(f"SUCCESSFULLY EXTRACTED ({len(hits)} reports):")
    print(f"{'='*60}")
    print(f"{'No':>5}  {'Year':<10}  {'Conf':<8}  {'MWCD Proj':>12}  {'Fin Alloc':>12}  {'Cut%':>8}")
    for no, yr, conf, proj, alloc, cut in sorted(hits, key=lambda x: x[1] or ""):
        print(f"{no:>5}  {str(yr):<10}  {conf:<8}  {str(proj):>12}  {str(alloc):>12}  {str(cut):>8}")

    print(f"\n{'='*60}")
    print(f"MISSED / NO EXTRACTION ({len(misses)} reports):")
    print(f"{'='*60}")
    for no, yr, note in misses:
        print(f"  Report {no} ({yr}): {note}")


if __name__ == "__main__":
    main()
