import argparse
import csv
import json
import re
from pathlib import Path

import pdfplumber


# Scheme name patterns
_ANGANWADI_RE = re.compile(
    r"(saksham\s+)?anganwadi\s*(services?|cum|and|&|\+|poshan)?",
    re.IGNORECASE,
)
_ICDS_RE = re.compile(r"\bicds\b|\bumbrella\s+icds\b", re.IGNORECASE)

# A rupee figure: digits with exactly 2 decimal places
_AMOUNT_RE = re.compile(r"[\d,]+\.\d{2}")


def _parse_float(s):
    if s is None:
        return None
    cleaned = re.sub(r"[^\d.]", "", s)
    try:
        return round(float(cleaned), 2)
    except ValueError:
        return None


def _extract_amounts_from_line(line, min_val=None):
    # Strip percentage markers before extracting, to avoid matching e.g. "45.63%"
    clean = re.sub(r"[\d,]+\.?\d*\s*%", "", line)
    amounts = [_parse_float(m) for m in _AMOUNT_RE.findall(clean)]
    if min_val is not None:
        amounts = [a for a in amounts if a and a >= min_val]
    return amounts


def _year_from_title(title):
    m = re.search(r"(\d{4}[-–]\d{2,4})", title)
    if m:
        raw = m.group(1)
        parts = re.split(r"[-–]", raw)
        if len(parts) == 2:
            start = parts[0]
            end = parts[1]
            if len(end) == 2:
                end = start[:2] + end
            return f"{start}-{end[-2:]}"
    return None


def extract_from_pdf(pdf_path):
    """
    Returns (mwcd_projected_cr, finance_allocated_cr, confidence) or (None, None, 'none').

    Strategy 1 (high): table with 'Projections to MoF' column — Report 365/377 style.
    Strategy 2 (medium): two-column 'Projected Demand | Actual Allocation' table — Report 338 style.
    Strategy 3 (low): find anganwadi-adjacent lines with two large amounts.
    """
    try:
        with pdfplumber.open(str(pdf_path)) as pdf:
            pages_text = []
            for page in pdf.pages:
                t = page.extract_text()
                pages_text.append(t or "")
    except Exception as e:
        return None, None, "none", f"pdfplumber error: {e}"

    full_text = "\n".join(pages_text)
    if not full_text.strip():
        return None, None, "none", "no text extracted (possibly scanned)"

    # ---- Strategy 1: TABLE-3 / Projections to MoF style ----
    # Look for pages that mention "Projections to MoF" or "Projections to Ministry of Finance"
    mof_proj_re = re.compile(r"projections?\s+to\s+(mof|ministry\s+of\s+finance)", re.IGNORECASE)
    for page_text in pages_text:
        if not mof_proj_re.search(page_text):
            continue
        lines = page_text.splitlines()
        for i, line in enumerate(lines):
            if not (_ANGANWADI_RE.search(line) or _ICDS_RE.search(line)):
                continue
            # Gather amounts from this line and next 1 line only (table may wrap mid-word)
            combined = " ".join(lines[i : i + 2])
            # Only crore-scale figures (>= 100); strip percentages first
            amounts = _extract_amounts_from_line(combined, min_val=100)
            # Table columns: actual, BE, RE, projection_to_MoF, BE_allocated
            # Need at least 4 amounts; projection is 2nd-to-last, allocated is last
            if len(amounts) >= 4:
                projected = amounts[-2]
                allocated = amounts[-1]
                if projected and allocated and projected > allocated:
                    return projected, allocated, "high", None
            # Fallback: if only 2 large amounts found
            if len(amounts) == 2 and amounts[0] > amounts[1]:
                return amounts[0], amounts[1], "high", None

    # ---- Strategy 2: Projected Demand | Actual Allocation table ----
    proj_alloc_re = re.compile(r"projected\s+(demand|be\b)", re.IGNORECASE)
    actual_alloc_re = re.compile(r"actual\s+allocation|allocated\s+be\b", re.IGNORECASE)
    # Also match integers (column-misaligned PDFs omit decimal for split tokens)
    _INT_OR_FLOAT_RE = re.compile(r"\b\d{3,}(?:\.\d{1,2})?\b")
    for page_text in pages_text:
        if not (proj_alloc_re.search(page_text) and actual_alloc_re.search(page_text)):
            continue
        lines = page_text.splitlines()
        for i, line in enumerate(lines):
            if not (_ANGANWADI_RE.search(line) or _ICDS_RE.search(line)):
                continue
            # Try single line only (strict decimal amounts >= 100)
            amounts = _extract_amounts_from_line(line, min_val=100)
            if len(amounts) >= 2:
                return amounts[0], amounts[1], "medium", None
            # Try including integers on this line (handles split-column PDFs)
            clean_line = re.sub(r"[\d,]+\.?\d*\s*%", "", line)
            raw_line = [_parse_float(m) for m in _INT_OR_FLOAT_RE.findall(clean_line)]
            big_line = [a for a in raw_line if a and a >= 100]
            if len(big_line) >= 2:
                return big_line[0], big_line[1], "medium", None
            # Last resort: include next line (for word-wrapped scheme names)
            combined = " ".join(lines[i : i + 2])
            amounts2 = _extract_amounts_from_line(combined, min_val=100)
            if len(amounts2) >= 2:
                return amounts2[0], amounts2[1], "medium", None

    # ---- Strategy 3: heuristic — anganwadi/ICDS line preceded by large-figure rows ----
    # Only look at the scheme-name line itself (not joined), and require amounts > 500 cr
    # to avoid picking up sub-component or nutrition-supplement figures.
    lines = full_text.splitlines()
    for i, line in enumerate(lines):
        if not (_ANGANWADI_RE.search(line) or _ICDS_RE.search(line)):
            continue
        # Try the current line alone first
        amounts = _extract_amounts_from_line(line, min_val=500)
        if len(amounts) >= 2:
            big_sorted = sorted(amounts, reverse=True)
            return big_sorted[0], big_sorted[1], "low", None
        # Then look at the full 3-line window
        combined = " ".join(lines[i : i + 3])
        amounts = _extract_amounts_from_line(combined, min_val=500)
        if len(amounts) >= 2:
            big_sorted = sorted(amounts, reverse=True)
            return big_sorted[0], big_sorted[1], "low", None

    return None, None, "none", "no matching table found"


